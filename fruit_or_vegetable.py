product = input()

if product == "banana" or product == "apple" \
    or product == "kiwi" or product == "cherry"\
    or product == "lemon" or product == "grapes":
    print("fruit")
elif product == "tomato" or product == "cucumber"\
    or product == "papper" or product == "carrot":
    print("vegetable")
else:
    print("unknown")